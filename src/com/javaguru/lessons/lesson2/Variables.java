package com.javaguru.lessons.lesson2;

public class Variables {

    public static void main(String[] args) {
        int floor = 0;
        System.out.println("Floor = " + floor);
        byte bikeWheels = 2;
        System.out.println("Bike wheels = " + bikeWheels);
        short countOfWindows = 18000;
        System.out.println("Count of windows: " + countOfWindows);
        long countOfCities = 2000000;
        System.out.println("Count of cities: " + countOfCities);
        boolean hungry = false;
        System.out.println("Hungry = " + hungry);
        char firstLetter = 'a';
        System.out.println("First letter = " + firstLetter);
    }
}
